# Program to test the usage of if...elif...else conditional blocks.

# Progra to check if the input number is even or odd. Returns invalid input for 0.

num = int(input("Input the number : "))

if ( num == 0 ):
    print("Invalid Input")
elif ( num % 2 == 0 ):
    print("Even Number")
else:
    print("Odd number")
        

    
    
